package lesson4.task1;

public class Human {
    private String firstName;
    private String lastName;
    private int age;
    private int height;
    private int weight;

    public Human(String firstName, String lastName, int age, int height, int weight) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.height = height;
        this.weight = weight;
    }
}
